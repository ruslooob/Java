package main;

// класс, содержащий метод main может называться как угодно,
// но лучше дать ему осмысленное название, чтобы другие программисты
// могли понять по названию файла, где находится точка входа в программу.
public class Main {

  // точка входа в программу
  public static void main(String[] args) {
    System.out.println("Hello world");
  }

}
